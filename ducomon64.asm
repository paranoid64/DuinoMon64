    lda #$00
    rts

countinc:
    inc $fc
    lda $fc
    bne nextinc
    inc $fd
nextinc:
    rts

send_string:
  ldy #$04

countstring:
    iny
    lda command,y     ; http://www.xxx.de/c/command.user
    cmp #$00          ; Länge der Eingabe ermitteln und für die Senderoutine speichern
    bne countstring
    sty command+1
    ldy #$00

string_next:
    iny
    lda command-1,y   ; http://www.xxx.de/c/command.user
    jsr write_byte    ; an den ESP senden
    cpy command+1
    bne string_next
    rts

write_byte:
    sta $dd01   ; Bit 0..7: Userport Daten PB 0-7 schreiben

dowrite:
    lda $dd0d
    nop
    nop
    nop
    nop
    and #$10        ; Warten auf NMI FLAG2 = Byte wurde gelesen vom ESP
    beq dowrite
    rts

