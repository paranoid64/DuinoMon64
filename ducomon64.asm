charconvert:
    cmp #$20
    bne con0
    lda #$2e        ; replace spaces with .  (command.user)
con0:
    cmp #$c0
    bcs con2
    cmp #$40
    bcs con1
    rts
con1:
    clc
    adc #$20
    rts
con2:
    sec
    sbc #$80
    rts

