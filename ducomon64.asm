*=$080d

  ; WiC init
  lda $dd02
  ora #$01
  sta $dd02
  lda #$02 ; $02 = ~ 1 sec.timeout
  sta $a

  ; set to 25 line text mode and turn on the screen
  lda #$1B
  sta $d011

  ; disable SHIFT-Commodore
  lda #$80
  sta $0291

  ; set screen memory ($0400) and charset bitmap offset ($2000)
  lda #$18
  sta $d018

  ; set border color
  lda #$00
  sta $D020

  ; set background color
  lda #$00
  sta $D021

  ; draw screen
  lda #$00
  sta $fb
  sta $fd
  sta $f7

  lda #$28
  sta $fc

  lda #$04
  sta $fe

  lda #$e8
  sta $f9
  lda #$2b
  sta $fa

  lda #$d8
  sta $f8

  ldx #$00
  ldy #$00
  lda ($fb),y
  sta ($fd),y
  lda ($f9),y
  sta ($f7),y
  iny
  bne *-9

  inc $fc
  inc $fe
  inc $fa
  inc $f8

  inx
  cpx #$04
  bne *-24

  ; wait for keypress
  lda $c6
  beq *-2

  beq start

start:
    jsr $e544     ; Clr screen

  ; set border color
  lda #$00
  sta $d020

  ; set background color
  lda #$00
  sta $d021

  ; set text color
  lda #$05
  sta $0286
  beq sendcommand

sendcommand:
  jsr $e544     ; Clr screen

  ldx #24
  ldy #00
  clc           ; Carry-Flag löschen, damit Cursor setzen ausgeführt wird
  jsr $fff0

  lda $dd02
  ora #$04
  sta $dd02     ; Datenrichtung Port A PA2 auf Ausgang
  lda #$ff      ; Datenrichtung Port B Ausgang
  sta $dd03
  lda $dd00
  ora #$04      ; PA2 auf HIGH = ESP im Empfangsmodus
  sta $dd00

  jsr send_string    ; php script per http:// + "Eingabe" aufrufen und den Inhalt auf dem Bilschirm ausgeben

getanswer:
  jsr $e544     ; Clr screen
  lda #$00                ; Datenrichtung Port B Eingang
  sta $dd03
  lda $dd00
  and #251                ; PA2 auf LOW = ESP im Sendemodus
  sta $dd00

  jsr read_byte           ;; Dummy Byte - IRQ anschieben

  jsr read_byte
  sta $1000
  tay

  jsr read_byte
  sta $1001
  tax

errorcheck:                 ; Answer 2 Byte = No data available
    cpy #$00
    bne goread
    cpx #$00
    beq empty               ; Answer 2 Byte 00 00 = No data available
    cpx #$02                ; z.b. "!E" = Error
    bne goread

empty:
    jmp exit

goread:
    jsr read_byte
    jsr $ffd2
    dex
    bne goread
    dey
    cpy #$ff
    bne goread

endless:
  ldx #$17   ; Select row
  ldy #$00   ; Select column
  jsr $E50C ; Set cursor

  lda #$ff      ; Datenrichtung Port B Ausgang
  sta $dd03

  lda $dd00
  ora #$04      ; PA2 auf HIGH = ESP im Empfangsmodus
  sta $dd00

  ldy #$00
  lda #$00

clearbuffer:      ; Eingabepuffer für php antwort löschen
    sta buffer,y
    iny
    cpy #$20
    bne clearbuffer

    ldy #$00
